#This program prints all the times tables from 1 to 12. 



for number in range (1, 10):
  answer_1 = 1 * number
  answer_4 = 4 * number
  answer_7 = 7 * number
  answer_10 = 10 * number
  print ('1 x', number, '=', answer_1, '    \t4 x', number, '=', answer_4, '    \t7 x', number, '=', answer_7, '     10 x', number, '=', answer_10)
